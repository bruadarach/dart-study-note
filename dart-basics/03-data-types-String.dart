// Dart is statically typed, which means you need to declare the type of a variable before using it.

void main() {
  String firstName = "Sujeong";
  String lastName = "Ji";
  
  print(firstName);
  print(lastName);
}

// Addition (only possible)
print(firstName + ' ' + lastName); 
print('${firstName} ${lastName}'); 
print('$firstName $lastName'); // omit {} 